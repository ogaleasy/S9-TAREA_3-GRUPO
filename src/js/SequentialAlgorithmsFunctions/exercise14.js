function compararFrases(frase1, frase2) {
    let respuesta

    if (frase1 === frase2) {
        console.log("Las frases son iguales.");
        respuesta = "Las frases son iguales."
    } else {
        console.log("Las frases no son iguales.");
        respuesta = "Las frases no son iguales."
    }

    return respuesta
};
